# Модуль диспетчерских служб аэропорта Шарджа (OMSJ ATC)

## Описание
Модуль OMSJ_ATC реализует симуляцию работы диспетчерских служб аэропорта Шарджа (Sharjah, OMSJ) 
на позициях TOWER, APPROACH и DEPARTURE. Модуль использует реальные схемы SID/STAR/Approach, 
включая ILS/VOR и RNAV-процедуры, и обеспечивает голосовое взаимодействие с пилотом через 
SRS Speech-to-Text (STT).

## Требования
- DCS World (актуальная версия)
- MOOSE Framework (актуальная ветка Develop)
- SRS (SimpleRadioStandalone) с поддержкой STT

## Установка
1. Скопируйте папку OMSJ_ATC в директорию Scripts/Modules/ вашей установки DCS World
2. Убедитесь, что MOOSE Framework установлен и работает
3. Убедитесь, что SRS установлен и настроен

## Использование в миссии
Для использования модуля в миссии DCS World:

1. Добавьте в миссию скрипт инициализации MOOSE:
```lua
dofile(lfs.writedir()..[[Scripts\MOOSE.lua]])
```

2. Добавьте в миссию скрипт инициализации OMSJ_ATC:
```lua
dofile(lfs.writedir()..[[Scripts\Modules\OMSJ_ATC\OMSJ_ATC.lua]])
```

3. Настройте активную ВПП (опционально):
```lua
OMSJ_ATC:SetActiveRunway("12") -- или "30"
```

## Функциональность

### Диспетчерские службы
- **Tower (TWR)**: Контроль за рулением на ВПП, взлётами и посадками
- **Approach (APP)**: Назначение STAR и заходов на посадку (ILS/VOR/RNAV)
- **Departure (DEP)**: Контроль взлетающих, назначение SID

### Взаимодействие с пилотом
- **Голосовое взаимодействие**: Через SRS STT
- **Меню F10**: Альтернативный интерфейс при невозможности использовать голос

### Процедуры
- **SID**: ANVIX1C, DESDI1C, EMERU1C, GIDIS1T, IVURO1T, LAGSA1T
- **STAR**: ULDOT1C, TONVO1C, IMDIL1T, GETBO1T
- **Approach**: ILS12, VOR12, RNAV12, ILS30, VOR30, RNAV30

## Частоты
- **ATIS**: 126.75 MHz
- **Ground**: 121.90 MHz
- **Tower**: 118.60 MHz
- **Approach**: 120.25 MHz
- **Departure**: 126.20 MHz

## Тестирование
Для проверки работоспособности модуля используйте тестовый скрипт:
```lua
dofile(lfs.writedir()..[[Scripts\Modules\OMSJ_ATC\OMSJ_ATC_Test.lua]])
```

## Расширение
Модуль спроектирован с возможностью масштабирования под другие аэропорты. 
Для добавления нового аэропорта необходимо создать аналогичную структуру файлов 
с соответствующими данными о путевых точках, процедурах и частотах.

